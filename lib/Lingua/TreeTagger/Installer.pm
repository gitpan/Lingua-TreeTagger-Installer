=head1 NAME

=encoding utf8

Lingua::TreeTagger::Installer - An installer tool for TreeTagger

=head1 DESCRIPTION

This module is not usable. It is for installation only.  It is used to
install TreeTagger and TreeTagger parameter files.

If you forgot to install some parameter file or want to update you
have two options: (1) install or update manually; (2) reinstall ths
module (you might need to use the C<< --force >> option on CPAN.

